/// Parse the todo tool's `items` argument into a validated list.
fn parse_todo_items(args: &serde_json::Value) -> Result<Vec<temple_protocol::TodoItem>, String> {
    let raw = args["items"].as_array().ok_or("missing items array")?;
    let mut items = Vec::with_capacity(raw.len());
    for (i, entry) in raw.iter().enumerate() {
        let content = entry["content"]
            .as_str()
            .ok_or_else(|| format!("items[{i}]: missing content"))?;
        if content.trim().is_empty() {
            return Err(format!("items[{i}]: content is empty"));
        }
        let status = match entry["status"].as_str().unwrap_or("pending") {
            "pending" => temple_protocol::TodoStatus::Pending,
            "in_progress" => temple_protocol::TodoStatus::InProgress,
            "done" => temple_protocol::TodoStatus::Done,
            other => return Err(format!("items[{i}]: invalid status '{other}'")),
        };
        items.push(temple_protocol::TodoItem {
            content: content.to_string(),
            status,
        });
    }
    Ok(items)
}

/// Render a todo list as a compact checklist for the model.
fn render_todo_list(items: &[temple_protocol::TodoItem]) -> String {
    if items.is_empty() {
        return "Todo list cleared.".into();
    }
    let done = items
        .iter()
        .filter(|i| i.status == temple_protocol::TodoStatus::Done)
        .count();
    let mut out = format!("Todo list ({done}/{} done):\n", items.len());
    for item in items {
        let mark = match item.status {
            temple_protocol::TodoStatus::Pending => "[ ]",
            temple_protocol::TodoStatus::InProgress => "[~]",
            temple_protocol::TodoStatus::Done => "[x]",
        };
        out.push_str(&format!("{mark} {}\n", item.content));
    }
    out.trim_end().to_string()
}

/// Repair a loaded history whose final tool-exchange is incomplete:
/// assistant messages carrying tool_calls with no matching tool_result
/// (a legacy cancel-mid-batch corruption). Appends synthetic error results
/// so the API accepts the history again, and drops any trailing tool
/// results that have no parent tool_call (orphaned by the same bug).
fn repair_dangling_tool_calls(mut history: Vec<ChatMessage>, session_id: Uuid) -> Vec<ChatMessage> {
    // Collect every tool_call id issued and every tool_result id answered.
    let mut issued: Vec<(String, String)> = Vec::new(); // (id, name) in order
    let mut answered: std::collections::HashSet<String> = std::collections::HashSet::new();
    for m in &history {
        if let Some(calls) = &m.tool_calls {
            for tc in calls {
                issued.push((tc.id.clone(), tc.function.name.clone()));
            }
        }
        if let Some(id) = &m.tool_call_id {
            answered.insert(id.clone());
        }
    }
    let missing: Vec<(String, String)> = issued
        .into_iter()
        .filter(|(id, _)| !answered.contains(id))
        .collect();
    if missing.is_empty() {
        return history;
    }
    tracing::warn!(
        "session {session_id}: repairing {} dangling tool call(s) in loaded history",
        missing.len()
    );
    for (id, name) in missing {
        history.push(ChatMessage::tool_result(
            id,
            name,
            "ERROR: tool call was interrupted — no result was recorded.".to_string(),
        ));
    }
    history
}

/// Detect project type from CWD contents and return language-specific context.
async fn detect_project_context(cwd: &str) -> String {
    let cwd_path = std::path::Path::new(cwd);
    let mut detected: Vec<String> = Vec::new();

    // Check for Rust
    if tokio::fs::try_exists(cwd_path.join("Cargo.toml"))
        .await
        .unwrap_or(false)
    {
        detected.push("Rust project detected (Cargo.toml found). Follow Rust conventions.".into());
    }

    // Check for Nix flake
    if tokio::fs::try_exists(cwd_path.join("flake.nix"))
        .await
        .unwrap_or(false)
    {
        detected
            .push("Nix flake detected. Use nix run/nix shell/nix build. Never nix-shell.".into());
    }

    // Check for ROOT/C++
    let has_cpp = tokio::fs::try_exists(cwd_path.join("CMakeLists.txt"))
        .await
        .unwrap_or(false);
    let has_cpp_ext = if !has_cpp {
        let mut entries = tokio::fs::read_dir(cwd_path).await.ok();
        let mut found = false;
        if let Some(entries) = entries.as_mut() {
            while let Ok(Some(e)) = entries.next_entry().await {
                let n = e.file_name().to_string_lossy().to_string();
                if n.ends_with(".C") || n.ends_with(".cpp") || n.ends_with(".cxx") {
                    found = true;
                    break;
                }
            }
        }
        found
    } else {
        true
    };
    if has_cpp_ext {
        detected.push("ROOT/C++ project detected. Use ROOT types (Int_t, Double_t, etc.). No modern C++ (no auto, no smart pointers, no lambdas).".into());
    }

    // Check for Python
    let has_py = tokio::fs::try_exists(cwd_path.join("pyproject.toml"))
        .await
        .unwrap_or(false)
        || tokio::fs::try_exists(cwd_path.join("setup.py"))
            .await
            .unwrap_or(false);
    let has_py_ext = if !has_py {
        let mut entries = tokio::fs::read_dir(cwd_path).await.ok();
        let mut found = false;
        if let Some(entries) = entries.as_mut() {
            while let Ok(Some(e)) = entries.next_entry().await {
                if e.file_name().to_string_lossy().ends_with(".py") {
                    found = true;
                    break;
                }
            }
        }
        found
    } else {
        true
    };
    if has_py_ext {
        detected.push("Python project detected.".into());
    }

    // Try to read project name from Cargo.toml
    if let Ok(content) = tokio::fs::read_to_string(cwd_path.join("Cargo.toml")).await {
        for line in content.lines() {
            if let Some(name) = line.strip_prefix("name = ") {
                detected.push(format!("Project name: {}", name.trim().trim_matches('"')));
                break;
            }
        }
    }

    if detected.is_empty() {
        String::new()
    } else {
        format!("\n## Project context\n{}\n", detected.join("\n"))
    }
}

fn summarize_args(args_json: &str) -> String {
    let v = LiteLLM::recover_tool_call(args_json);
    match v {
        Some(serde_json::Value::Object(map)) => {
            let mut lines: Vec<String> = Vec::new();
            for (k, val) in map.iter() {
                let val_str = match val {
                    serde_json::Value::String(s) => s.clone(),
                    other => other.to_string(),
                };
                let trunc: String = val_str.chars().take(100).collect();
                if trunc.len() < val_str.len() {
                    lines.push(format!("{k}: {trunc}…"));
                } else {
                    lines.push(format!("{k}: {trunc}"));
                }
            }
            lines.join("\n")
        }
        Some(other) => {
            let s = other.to_string();
            s.chars().take(120).collect()
        }
        None => args_json.chars().take(120).collect(),
    }
}

fn truncate(s: &str, max: usize) -> String {
    if s.len() <= max {
        s.to_string()
    } else {
        // Byte-slicing (&s[..max]) panics on multi-byte UTF-8 boundaries —
        // tool output is arbitrary file content, so cut by chars instead.
        let cut: String = s.chars().take(max).collect();
        format!("{cut}…[truncated {} bytes]", s.len() - cut.len())
    }
}

/// Deterministic session title fallback: first line of the first user
/// message, truncated at a word boundary. Returns "" for empty input.
fn fallback_title_from_msg(text: &str) -> String {
    let first_line = text.lines().next().unwrap_or("").trim();
    if first_line.is_empty() {
        return String::new();
    }
    if first_line.chars().count() <= 48 {
        return first_line.to_string();
    }
    let mut t: String = first_line.chars().take(48).collect();
    if let Some(pos) = t.rfind(' ') {
        t.truncate(pos);
    }
    t.push('…');
    t
}

/// Generate code rules that only cover the languages detected in the project
/// section. If no specific language is detected, returns an empty string —
/// no code rules are needed.
fn format_code_rules(project_section: &str) -> String {
    let mut rules = Vec::new();
    let hay = project_section.to_lowercase();

    if hay.contains("rust") {
        rules.push("### Rust\n- Follow the existing style of the surrounding code.\n- Handle errors properly — no .unwrap() in production paths.");
    }
    if hay.contains("nix") {
        rules.push("### Nix\n- Always use flakes and flake-based commands (nix run, nix shell, etc.).\n- Follow the existing style of the surrounding modules.");
    }
    if hay.contains("root/c++") || hay.contains("c++") || hay.contains("root") {
        rules.push("### C++ / ROOT\n- Use ROOT data types. No modern C++ (no auto, no smart pointers, no lambdas).");
    }
    if hay.contains("python") {
        rules.push("### Python\n- In Python that uses ROOT, never use matplotlib.");
    }

    if rules.is_empty() {
        String::new()
    } else {
        format!("\n{}", rules.join("\n\n"))
    }
}
