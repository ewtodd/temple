/// Resolves permission requests from the agent loop via the client.
pub struct PermissionResolver {
    pending: Mutex<HashMap<Uuid, oneshot::Sender<bool>>>,
}

impl PermissionResolver {
    pub fn new() -> Self {
        Self {
            pending: Mutex::new(HashMap::new()),
        }
    }

    pub async fn ask(&self, request_id: Uuid) -> oneshot::Receiver<bool> {
        let (tx, rx) = oneshot::channel();
        self.pending.lock().await.insert(request_id, tx);
        rx
    }

    pub async fn resolve(&self, request_id: Uuid, granted: bool) {
        if let Some(tx) = self.pending.lock().await.remove(&request_id) {
            let _ = tx.send(granted);
        }
    }

    /// Drop a pending request without resolving it — used when the waiter
    /// timed out or was cancelled, so the map doesn't leak entries.
    pub async fn forget(&self, request_id: Uuid) {
        self.pending.lock().await.remove(&request_id);
    }
}

/// Events the agent loop emits toward the client.
#[derive(Debug, Clone)]
pub enum AgentEvent {
    Delta(String),
    ToolEvent {
        name: String,
        status: ToolStatus,
        detail: String,
    },
    PermissionNeeded(PermissionRequest),
    Done(ChatStatsData),
    Error(String),
    /// A stream that already produced deltas failed and is being retried —
    /// clients should discard the partial message they accumulated.
    StreamReset,
    /// The session's todo list changed (full replacement).
    TodoUpdated(Vec<temple_protocol::TodoItem>),
    /// Server requests the client to execute a local tool
    ToolRequestNeeded {
        request_id: Uuid,
        name: String,
        args_json: String,
    },
}

#[derive(Debug, Clone)]
pub struct ChatStatsData {
    pub model: String,
    pub prompt_tokens: u32,
    pub completion_tokens: u32,
    pub duration_ms: u64,
    pub context_length: u32,
    pub prefill_tps: f64,
    pub decode_tps: f64,
}

/// Accumulates token/timing stats across multiple tool-loop rounds
/// and pipeline revision rounds.
struct StatsAccum {
    total_completion: u32,
    last_prompt_tokens: u32,
    total_prefill_ms: u64,
    total_decode_ms: u64,
    total_prefill_tokens: u32,
}

impl StatsAccum {
    fn new() -> Self {
        Self {
            total_completion: 0,
            last_prompt_tokens: 0,
            total_prefill_ms: 0,
            total_decode_ms: 0,
            total_prefill_tokens: 0,
        }
    }

    fn prefill_tps(&self) -> f64 {
        if self.total_prefill_ms > 0 {
            self.total_prefill_tokens as f64 / (self.total_prefill_ms as f64 / 1000.0)
        } else {
            0.0
        }
    }

    fn decode_tps(&self) -> f64 {
        if self.total_decode_ms > 0 {
            self.total_completion as f64 / (self.total_decode_ms as f64 / 1000.0)
        } else {
            0.0
        }
    }
}

struct SessionCtx {
    model: String,
    history: Vec<ChatMessage>,
    username: String,
    cwd: String,
    kind: SessionKind,
    /// Client account name (e.g. "e-play") for per-account session clearing
    account: Option<String>,
    /// Person who owns this session (token username, e.g. "ethan").
    owner: String,
    /// Session title (generated after first exchange)
    title: Option<String>,
    /// True when the title was generated by the router model. Fallback
    /// titles (truncated first message) leave this false so
    /// ensure_session_title knows it can still try the model.
    title_from_model: bool,
    /// Whether this session should be persisted to the DB
    persist: bool,
    /// Permission mode, persisted and restored on resume.
    permission_mode: PermissionMode,
    /// Cached project context string (detected from CWD on first message)
    project_context: Option<String>,
    /// True when the user explicitly picked a model via /model — routing
    /// is bypassed and this model is used directly.  /model auto clears it.
    model_override: bool,
    /// Model locked by the router for the session. The first substantive
    /// (non-Simple, non-command) message stamps this, and all subsequent
    /// messages reuse it without re-classifying.  Cleared by /model and
    /// on reset.  In-memory only — not persisted to DB.
    last_routed_model: Option<String>,
    /// Coding-session task list, persisted with the session.
    todos: Vec<temple_protocol::TodoItem>,
}

pub struct Agent {
    pub litellm: LiteLLM,
    pub mcp: McpClient,
    pub memory: Arc<Memory>,
    pub permissions: Arc<PermissionResolver>,
    sessions: Mutex<HashMap<Uuid, SessionCtx>>,
    /// Per-session cancellation tokens for in-progress agent loops.
    /// The generation counter guards against a stale loop deleting the
    /// NEW loop's token during cleanup (remove only if generation matches).
    cancel_tokens: Mutex<HashMap<Uuid, (u64, CancellationToken)>>,
    /// Global generation counter for cancel tokens.
    token_generation: std::sync::atomic::AtomicU64,
    /// Per-session loop locks — one agent loop runs per session at a time.
    /// A second message cancels the first loop's token and then waits for
    /// the lock, so the two loops never interleave history writes.
    loop_locks: Mutex<HashMap<Uuid, Arc<tokio::sync::Mutex<()>>>>,
    /// Pending tool requests: request_id → oneshot sender for the result
    pending_tools: Mutex<HashMap<Uuid, oneshot::Sender<String>>>,
    pub models: ModelConfig,
    /// Global priority queue — one agent loop at a time across all sessions
    pub queue: crate::queue::RequestQueue,
    /// Last time a real request started (keep-warm skips while this is fresh)
    last_request: std::sync::Mutex<Instant>,
    tools: Mutex<Vec<ToolDefinition>>,
    /// Active daemon connections: username → count (multiple sessions per user).
    /// Used for Signal sandboxing — users without a daemon get restricted tools.
    daemon_connections: Mutex<HashMap<String, u32>>,
    /// Pending web authentication: code → (tx_channel, expiry).
    /// The tx channel is used to notify the WebSocket connection when
    /// verification succeeds via Signal.
    pub pending_web_auth: tokio::sync::Mutex<
        HashMap<
            String,
            (
                tokio::sync::mpsc::UnboundedSender<ServerMessage>,
                std::time::Instant,
            ),
        >,
    >,
}

